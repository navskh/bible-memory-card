const fs = require('fs');
const path = require('path');

// 간단한 PNG 데이터 (1x1 픽셀, 투명)
const simplePNG = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
  0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
  0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
  0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

// 아이콘 크기들
const sizes = [192, 512];

// 아이콘 디렉토리 생성
const iconsDir = path.join(__dirname, '../public/icons');
if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// 각 크기별로 아이콘 생성
sizes.forEach(size => {
  const iconPath = path.join(iconsDir, `icon-${size}x${size}.png`);
  
  // 실제로는 더 큰 PNG를 생성해야 하지만, 테스트를 위해 간단한 PNG 사용
  // 실제 프로덕션에서는 sharp 라이브러리를 사용하여 SVG를 PNG로 변환해야 합니다
  fs.writeFileSync(iconPath, simplePNG);
  console.log(`Created ${iconPath}`);
});

console.log('\n아이콘 생성 완료!');
console.log('참고: 실제 프로덕션에서는 sharp 라이브러리를 사용하여 SVG를 PNG로 변환해야 합니다.');
console.log('현재는 테스트용으로 간단한 PNG를 생성했습니다.'); 